-- ■ 函数名称: Platoon000120_Activate
-- ■ 功能描述: 激活并初始化队伍编制120号的环形包围阵型
-- ■ 参数说明:
-- ■   f1_arg0: 队伍管理器对象，负责控制整个包围大队的协调行为
-- ■ 执行逻辑:
-- ■   1. 启用队伍协同移动机制
-- ■   2. 设置环形包围阵型
-- ■   3. 配置9个位置的环形坐标
-- ■   4. 设置协调移动速度模式
function Platoon000120_Activate(f1_arg0)
    -- ■ 启用队伍协同移动：确保包围过程中阵型完整性
    -- ■ 对于环形包围，位置协调至关重要
    f1_arg0:SetEnablePlatoonMove(true)

    -- ■ 设置阵型类型：采用类型2的楔形基础，扩展为环形包围
    -- ■ 通过多点位配置实现360度包围效果
    f1_arg0:SetFormationType(0, 2)

    -- ■ 配置队伍位置0（中央指挥官）：包围圈中心指挥位置
    -- ■ 坐标(0, 0): 作为包围圈中心，统筹指挥全方位作战
    -- ■ 负责监控包围效果和协调各方向的攻击时机
    f1_arg0:SetFormationParam(0, 0, 0)

    -- ■ 配置队伍位置1（前方哨兵）：正前方包围点
    -- ■ 坐标(0, 2): X轴中央，Z轴前进2米
    -- ■ 承担正面阻截和前方观察任务
    f1_arg0:SetFormationParam(1, 0, 2)

    -- ■ 配置队伍位置2（右前包围）：右前方45度位置
    -- ■ 坐标(1, 1): X轴右侧1米，Z轴前进1米
    -- ■ 负责右前方向的包围和切断退路
    f1_arg0:SetFormationParam(2, 1, 1)

    -- ■ 配置队伍位置3（右侧主力）：右侧90度包围点
    -- ■ 坐标(2, 0): X轴右侧2米，Z轴中央
    -- ■ 形成右侧主要包围力量，阻断右方逃生路线
    f1_arg0:SetFormationParam(3, 2, 0)

    -- ■ 配置队伍位置4（右后包围）：右后方135度位置
    -- ■ 坐标(1, -1): X轴右侧1米，Z轴后退1米
    -- ■ 负责右后方向的包围和后路切断
    f1_arg0:SetFormationParam(4, 1, -1)

    -- ■ 配置队伍位置5（后方封锁）：正后方180度位置
    -- ■ 坐标(0, -2): X轴中央，Z轴后退2米
    -- ■ 承担后方封锁和防止目标后撤的关键任务
    f1_arg0:SetFormationParam(5, 0, -2)

    -- ■ 配置队伍位置6（左后包围）：左后方225度位置
    -- ■ 坐标(-1, -1): X轴左侧1米，Z轴后退1米
    -- ■ 负责左后方向的包围和侧后切断
    f1_arg0:SetFormationParam(6, -1, -1)

    -- ■ 配置队伍位置7（左侧主力）：左侧270度包围点
    -- ■ 坐标(-2, 0): X轴左侧2米，Z轴中央
    -- ■ 形成左侧主要包围力量，封锁左方退路
    f1_arg0:SetFormationParam(7, -2, 0)

    -- ■ 配置队伍位置8（左前包围）：左前方315度位置
    -- ■ 坐标(-1, 1): X轴左侧1米，Z轴前进1米
    -- ■ 负责左前方向的包围和前路阻截
    f1_arg0:SetFormationParam(8, -1, 1)

    -- ■ 设置队伍协调移动速度：1.2倍标准移动速度
    -- ■ 适中的速度确保包围过程中阵型稳定性
    -- ■ 既保证包围效率又维持包围圈的完整性
    f1_arg0:SetBaseMoveRate(0, 1.2)

end

