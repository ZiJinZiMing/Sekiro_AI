-- ■ 函数名称: Platoon000130_Activate
-- ■ 功能描述: 激活并初始化队伍编制130号的高速交替突击阵型
-- ■ 参数说明:
-- ■   f1_arg0: 队伍管理器对象，负责控制整个高速突击小队的协调行为
-- ■ 执行逻辑:
-- ■   1. 启用队伍协同移动机制
-- ■   2. 设置左右交替的高速突击阵型
-- ■   3. 配置6个位置的锯齿形坐标
-- ■   4. 设置极高速移动模式（4倍速度）
function Platoon000130_Activate(f1_arg0)
    -- ■ 启用队伍协同移动：在极高速移动中保持阵型协调
    -- ■ 对于4倍速度的移动，精确的位置控制至关重要
    f1_arg0:SetEnablePlatoonMove(true)

    -- ■ 设置阵型类型：采用类型2楔形基础，优化为高速交替移动
    -- ■ 楔形基础配合交替配置，适应高速突击需求
    f1_arg0:SetFormationType(0, 2)

    -- ■ 配置队伍位置0（高速指挥官）：突击阵型的先导位置
    -- ■ 坐标(0, 0): 作为高速突击的引导者，承担路线选择和风险评估
    -- ■ 在4倍速度下需要极强的反应能力和战场感知
    f1_arg0:SetFormationParam(0, 0, 0)

    -- ■ 配置队伍位置1（左翼第一梯队）：左侧前进位置
    -- ■ 坐标(-1, -1): X轴左侧1米，Z轴后退1米
    -- ■ 形成左翼第一冲击波，与指挥官形成小型突击组
    f1_arg0:SetFormationParam(1, -1, -1)

    -- ■ 配置队伍位置2（右翼第一梯队）：右侧前进位置
    -- ■ 坐标(1, -1): X轴右侧1米，Z轴后退1米
    -- ■ 形成右翼第一冲击波，与左翼呈对称分布
    f1_arg0:SetFormationParam(2, 1, -1)

    -- ■ 配置队伍位置3（左翼第二梯队）：左侧深层支援
    -- ■ 坐标(-1, -3): X轴左侧1米，Z轴后退3米
    -- ■ 形成左翼第二冲击波，确保左侧推进的持续性
    f1_arg0:SetFormationParam(3, -1, -3)

    -- ■ 配置队伍位置4（右翼第二梯队）：右侧深层支援
    -- ■ 坐标(1, -3): X轴右侧1米，Z轴后退3米
    -- ■ 形成右翼第二冲击波，与左翼保持对称和平衡
    f1_arg0:SetFormationParam(4, 1, -3)

    -- ■ 配置队伍位置5（左翼殿后）：左侧最后梯队
    -- ■ 坐标(-1, -5): X轴左侧1米，Z轴后退5米
    -- ■ 提供最后的左翼支援和撤退时的掩护
    f1_arg0:SetFormationParam(5, -1, -5)

    -- ■ 设置队伍极高速移动模式：4倍标准移动速度
    -- ■ 这是所有阵型中最高的移动速度配置
    -- ■ 适用于闪电战、紧急撤退、快速支援等关键时刻
    f1_arg0:SetBaseMoveRate(0, 4)

end

