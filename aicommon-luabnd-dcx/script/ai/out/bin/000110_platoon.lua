-- ■ 函数名称: Platoon000110_Activate
-- ■ 功能描述: 激活并初始化队伍编制110号的右翼突击阵型
-- ■ 参数说明:
-- ■   f1_arg0: 队伍管理器对象，负责控制整个突击小队的协调行为
-- ■ 执行逻辑:
-- ■   1. 启用队伍协同移动机制
-- ■   2. 设置右翼加强的楔形突击阵型
-- ■   3. 配置6个位置的战术坐标
-- ■   4. 设置高速移动模式（1.5倍速度）
function Platoon000110_Activate(f1_arg0)
    -- ■ 启用队伍协同移动：确保突击过程中阵型完整性
    -- ■ 对于高速突击作战，阵型协调尤为重要
    f1_arg0:SetEnablePlatoonMove(true)

    -- ■ 设置阵型类型：采用类型2的楔形阵型，针对突击作战优化
    -- ■ 楔形阵型利于突破敌人防线和集中火力
    f1_arg0:SetFormationType(0, 2)

    -- ■ 配置队伍位置0（主攻指挥官）：突击矛头位置
    -- ■ 坐标(0, 0): 作为突击箭头，承担主要突破任务
    -- ■ 负责识别突破口和指挥整个突击行动
    f1_arg0:SetFormationParam(0, 0, 0)

    -- ■ 配置队伍位置1（副指挥官）：紧密支援位置
    -- ■ 坐标(0, 0): 与主指挥官重合，提供火力倍增和指挥备份
    -- ■ 在主指挥官受阻时可立即接管突击指挥
    f1_arg0:SetFormationParam(1, 0, 0)

    -- ■ 配置队伍位置2（右翼突击手）：主要突击力量
    -- ■ 坐标(2, -1): X轴右侧2米，Z轴后退1米
    -- ■ 形成右翼主攻方向，与指挥官协同突破
    f1_arg0:SetFormationParam(2, 2, -1)

    -- ■ 配置队伍位置3（中央支援）：后方火力支援
    -- ■ 坐标(0, -3): X轴中央，Z轴后退3米
    -- ■ 提供中央支援火力，保持与前锋的火力连续性
    f1_arg0:SetFormationParam(3, 0, -3)

    -- ■ 配置队伍位置4（右翼深入）：右侧深层突击
    -- ■ 坐标(2, -4): X轴右侧2米，Z轴后退4米
    -- ■ 形成右翼第二梯队，负责扩大突破口和侧翼掩护
    f1_arg0:SetFormationParam(4, 2, -4)

    -- ■ 配置队伍位置5（后卫支援）：深层支援和掩护
    -- ■ 坐标(1, -7): X轴右侧1米，Z轴后退7米
    -- ■ 提供最深层的火力支援和撤退时的殿后掩护
    f1_arg0:SetFormationParam(5, 1, -7)

    -- ■ 设置队伍高速移动模式：1.5倍标准移动速度
    -- ■ 高速度适应突击作战的快速推进需求
    -- ■ 在敌人反应之前完成位置占领和突破
    f1_arg0:SetBaseMoveRate(0, 1.5)

end

