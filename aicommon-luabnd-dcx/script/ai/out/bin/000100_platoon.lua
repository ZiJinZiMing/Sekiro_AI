-- ■ 函数名称: Platoon000100_Activate
-- ■ 功能描述: 激活并初始化队伍编制100号的作战阵型
-- ■ 参数说明:
-- ■   f1_arg0: 队伍管理器对象，负责控制整个小队的协调行为
-- ■ 执行逻辑:
-- ■   1. 启用队伍协同移动机制
-- ■   2. 设置V字型作战阵型（阵型类型2）
-- ■   3. 配置6个位置的具体坐标和间距
-- ■   4. 设置队伍移动速度倍率
function Platoon000100_Activate(f1_arg0)
    -- ■ 启用队伍协同移动：所有队员将保持阵型进行移动
    -- ■ true表示启用，队员会自动调整位置以维持阵型完整性
    f1_arg0:SetEnablePlatoonMove(true)

    -- ■ 设置阵型类型：阵型ID=0使用类型2的V字型楔形阵型
    -- ■ 参数0: 阵型配置索引，参数2: V字型阵型类型
    -- ■ V字型阵型适合突击作战和前进掩护
    f1_arg0:SetFormationType(0, 2)

    -- ■ 配置队伍位置0（指挥官位置）：阵型中心前锋点
    -- ■ 坐标(0, 0): X轴0米，Z轴0米 - 作为整个阵型的基准参考点
    -- ■ 指挥官通常承担前锋侦察和火力引导任务
    f1_arg0:SetFormationParam(0, 0, 0)

    -- ■ 配置队伍位置1（副指挥官位置）：紧随指挥官的支援位置
    -- ■ 坐标(0, 0): 与指挥官重合，提供即时火力支援和指挥协助
    -- ■ 在指挥官受到威胁时可以立即接管指挥权
    f1_arg0:SetFormationParam(1, 0, 0)

    -- ■ 配置队伍位置2（右翼前锋）：V字型右侧前进位置
    -- ■ 坐标(2, -2): X轴右侧2米，Z轴后退2米
    -- ■ 负责右侧警戒和侧翼攻击，与指挥官形成交叉火力
    f1_arg0:SetFormationParam(2, 2, -2)

    -- ■ 配置队伍位置3（左翼前锋）：V字型左侧前进位置
    -- ■ 坐标(-2, -3): X轴左侧2米，Z轴后退3米
    -- ■ 负责左侧警戒和侧翼攻击，稍微后置以形成梯次
    f1_arg0:SetFormationParam(3, -2, -3)

    -- ■ 配置队伍位置4（右翼后卫）：右侧纵深支援位置
    -- ■ 坐标(1, -5): X轴右侧1米，Z轴后退5米
    -- ■ 提供右翼纵深火力支援和后方警戒
    f1_arg0:SetFormationParam(4, 1, -5)

    -- ■ 配置队伍位置5（左翼后卫）：左侧纵深支援位置
    -- ■ 坐标(-1, -6): X轴左侧1米，Z轴后退6米
    -- ■ 提供左翼纵深火力支援和殿后掩护
    f1_arg0:SetFormationParam(5, -1, -6)

    -- ■ 设置队伍基础移动速度：1.2倍标准速度
    -- ■ 参数0: 应用于所有队员，参数1.2: 移动速度倍率
    -- ■ 提高移动效率，适应快节奏战斗环境
    f1_arg0:SetBaseMoveRate(0, 1.2)

end

